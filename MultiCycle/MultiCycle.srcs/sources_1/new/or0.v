module or0 
/*** PARAMETERS ***/
#(parameter
    DATA_WL     = 32,
    ADDR_RF_WL  = 5,
    ADDR_MEM    = 32,
    IMMED_WL    = 16,
    JUMP_WL     = 26
)
/*** IN/OUT ***/
(
    // IN
    input   rst,
            jump,
            PC_EN,
    // OUT
    output reg PCEN
);
    always @(*)
    begin
        if(rst) PCEN = 1'bx;
        else PCEN = jump | PC_EN;
    end
endmodule
